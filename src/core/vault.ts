import { existsSync, mkdirSync, writeFileSync, readdirSync, readFileSync } from 'node:fs';
import { join } from 'node:path';
import matter from 'gray-matter';

export interface TaskFrontmatter {
  id: string;
  title: string;
  status: 'backlog' | 'ready' | 'in-progress' | 'review' | 'deploying' | 'monitoring' | 'done';
  project: string;
  priority: 'critical' | 'high' | 'medium' | 'low';
  created: string;
  provider?: string;
  review_model?: string;
  tags: string[];
  complexity: 'low' | 'medium' | 'high';
  branch: string;
  deploy_env?: string;
}

const VAULT_DIRS = [
  '_oasis/templates',
  'projects',
  'wiki/concepts',
  'wiki/patterns',
  'wiki/entities',
  'wiki/summaries',
  'raw-sources',
];

const SCHEMA_TEMPLATE = `# Oasis Vault Schema

This vault is managed by Oasis — a provider-agnostic AI dev orchestrator.

## Structure

- \`projects/\` — One folder per project, each with backlog/, decisions/, skills/
- \`wiki/\` — Auto-maintained knowledge base (LLM Wiki pattern)
- \`raw-sources/\` — Articles, transcripts, images for ingestion
- \`_oasis/\` — Internal system files (templates, index)

## Task Status Flow

\`backlog → ready → in-progress → review → deploying → done\`

## Operations

- **Ingest**: Add raw sources → AI processes and updates wiki
- **Query**: Ask questions against the wiki
- **Lint**: Health check for contradictions, orphans, stale content
`;

